def divide(first_num, second_num):
    return f"{first_num / second_num:.2f}"


def multiply(first_num, second_num):
    return f"{first_num * second_num:.2f}"


def subtract(first_num, second_num):
    return f"{first_num - second_num:.2f}"


def add(first_num, second_num):
    return f"{first_num + second_num:.2f}"


def raise_numbers(first_num, second_num):
    return f"{first_num ** second_num:.2f}"

